-- Semicolons may optionally follow statements. Convention is to use them for
-- separating statements within the same line, as Line Breaks play no syntatical
-- role. The following are all equal and valid.

a = 1
b = a*2
print(b)

a = 1;
b = a*2;
print(b)

a = 1; b = a*2; print(b)

a = 1 b=a*2 print(b)



